package Chapter1.io;

public class Condition_Switch {

    public static void main(String[] args) {


        int number = 3;

        switch (number) {
            case 1:
                System.out.println("1입니다");
                break;
            case 2:
                System.out.println("2입니다");
                break;
            default:
                System.out.println("1과2 모두 아닙니다");
        }
    }
}
